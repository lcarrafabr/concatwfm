    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        CadastroCelulas oCadCel = new CadastroCelulas();
        
        if(celulaIdjTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "O campo Cod. Cel. é obrigatório.", "Mensgem do sistema", JOptionPane.WARNING_MESSAGE);
        }else if(celulaNomejTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "O campo Nome Célula. é obrigatório.", "Mensgem do sistema", JOptionPane.WARNING_MESSAGE);
        }else if(operacaojComboBox.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "O campo OPERAÇÃO é obrigatório.", "Mensgem do sistema", JOptionPane.WARNING_MESSAGE);
        }else{
            
            try {

                salvarjButton.setVisible(false);
                novojButton.setVisible(true);
                editarjButton.setVisible(false);
                cancelarjButton.setVisible(false);
                
                combo_operacao = (ComboItem)operacaojComboBox.getSelectedItem();
                
                oCadCel.setCelula_id(Integer.parseInt(celulaIdjTextField.getText().trim()));
                oCadCel.setOperacao_id(combo_operacao.getId());
                oCadCel.setCelula_nome(celulaNomejTextField.getText().trim().toUpperCase());
                oCadCel.setCadastrar();                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar\n"+e, "Mensgem do sistema", JOptionPane.ERROR_MESSAGE);                
            }
        }
        
        atualizaGrade();
        
    }//GEN-LAST:event_salvarjButtonActionPerformed

