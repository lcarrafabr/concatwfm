    public void getGrandeCelulas() throws ParseException {
        //Consultar Celulas
        CadastroCelulas oCadCel = new CadastroCelulas();

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Cod. Célula", "Célula", "Operação"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeCelulajTable.setModel(modeloTabela);
        gradeCelulajTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeCelulajTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        gradeCelulajTable.getColumnModel().getColumn(2).setPreferredWidth(50);

        try {

            resultSet = oCadCel.getConsultar();

            if (resultSet.getRow() == 1) {
                resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                            resultSet.getString("CELULA_ID"),
                            resultSet.getString("CELULA_NOME"),
                            resultSet.getString("OPERACAO_NAME")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(JFCadastroCelula.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    public void getFieldCelula(int click_celula) throws ParseException {

        CadastroCelulas oCadCel = new CadastroCelulas();
        ResultSet rs;
        oCadCel.setCelula_id(click_celula);

        try {
            rs = oCadCel.getConsultar();
            rs.next();

            //Pegar campos
            ComboItem oItem = new ComboItem(rs.getInt(1), rs.getString(2));
            operacaojComboBox.setSelectedItem(oItem);
            
            celulaIdjTextField.setText(Integer.toString(rs.getInt("CELULA_ID")));
            celulaNomejTextField.setText(rs.getString("CELULA_NOME"));

        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void atualizaGrade(){
        try {
            getGrandeCelulas();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar grade.\n"+e, "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        }
        
        int contador_registros = gradeCelulajTable.getRowCount();
        contadorRegistrosjLabel.setText(Integer.toString(contador_registros));
    }
    
    private void atualizarComboOperacao(){
        
        Operacoes oOperacoes = new Operacoes();
        
        ResultSet rs;
        
        try {
            
            rs = oOperacoes.getConsultarIdOperacao();
            operacaojComboBox.removeAllItems();
            while (rs.next()) {                
                
                operacaojComboBox.addItem(new ComboItem(rs.getInt(1), rs.getString(2)));
            }
            operacaojComboBox.updateUI();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar combo operações.\n"+e, 
                    "Mensagem do sistema", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
