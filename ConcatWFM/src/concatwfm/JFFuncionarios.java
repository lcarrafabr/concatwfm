    private void matriculajTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_matriculajTextFieldKeyReleased
        // TODO add your handling code here:
        String caracteres = matriculajTextField.getText().trim();
        int qtd_caracteres = caracteres.length();

        if (qtd_caracteres >= 9) {
            JOptionPane.showMessageDialog(null, "Atenção!\n"
                    + "O campo matrícula não pode ter mais que 9 caracteres.",
                    "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);

            caracteres = caracteres.substring(0, caracteres.length() - 1);
            matriculajTextField.setText("");
            matriculajTextField.setText(caracteres);

        } else {

            if (!"".equals(matriculajTextField.getText().trim())) {
                try {
                    Funcionarios oFunc = new Funcionarios();
                    oFunc.setMatricula(Integer.parseInt(matriculajTextField.getText()));
                    ResultSet rs = null;
                    rs = oFunc.getConsultar();
                    getGrandeFuncionarios();
                } catch (ParseException | SQLException ex) {
                    Logger.getLogger(JFFuncionarios.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                buscarjButtonActionPerformed(null);
            }
        }
    }//GEN-LAST:event_matriculajTextFieldKeyReleased

