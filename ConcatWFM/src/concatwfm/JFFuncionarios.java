                oFunc.setGmid(gmidjTextField.getText().trim().toUpperCase());
                oFunc.setNiceID(niceIDjTextField.getText().trim());

                if (loginAvayajTextField.getText().isEmpty()) {
                    oFunc.setLogin_avaya(0);
                } else {
                    oFunc.setLogin_avaya(Integer.parseInt(loginAvayajTextField.getText().trim()));
                }

                oFunc.setNome(nomejTextField.getText().trim().toUpperCase());
                oFunc.setHiredate(Funcoes.getDataFormatBR(hiredatejFormattedTextField.getText()));
                oFunc.setNetwork(networkjTextField.getText().trim().toUpperCase());
                oFunc.setEmail(emailjTextField.getText().trim().toLowerCase());
                oFunc.setIdccms(idccms);
                oFunc.setId_spreadfast(idSpreadFastTextField2.getText().trim().toUpperCase());
