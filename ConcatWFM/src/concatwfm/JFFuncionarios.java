    public void getGrandeFuncionarios() throws ParseException {
        //Consultar pedidos
        Funcionarios oFunc = new Funcionarios();

        if (nomejTextField.getText().length() > 0) {
            oFunc.setNome(nomejTextField.getText() + "%");
        }
        if (gmidjTextField.getText().length() > 0) {
            oFunc.setGmid(gmidjTextField.getText() + "%");
        }
        if (matriculajTextField.getText().length() != 0) {

            oFunc.setMatricula(Integer.valueOf(matriculajTextField.getText()));
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"Matrícula", "Nome funcionário", "Login de rede TP", "GMID"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeFuncionariojTable.setModel(modeloTabela);
        gradeFuncionariojTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeFuncionariojTable.getColumnModel().getColumn(1).setPreferredWidth(250);
        gradeFuncionariojTable.getColumnModel().getColumn(2).setPreferredWidth(10);
        gradeFuncionariojTable.getColumnModel().getColumn(3).setPreferredWidth(10);

        try {

            resultSet = oFunc.getConsultar();

            if (resultSet.getRow() == 1) {
                resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                            resultSet.getString("MATRICULA"),
                            resultSet.getString("NOME"),
                            resultSet.getString("NETWORK"),
                            resultSet.getString("GMID"),});

            }

        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void getFieldFuncionario(int click_matricula) throws ParseException {

        Funcionarios oFunc = new Funcionarios();
        ResultSet rs;
        oFunc.setMatricula(click_matricula);

        try {
            rs = oFunc.getConsultar();
            rs.next();

            //Pegar campos
            nomejTextField.setText(rs.getString("NOME"));
            hiredatejFormattedTextField.setText(Funcoes.getDataFormat(rs.getDate("HIREDATE")));
            matriculajTextField.setText(Integer.toString(rs.getInt("MATRICULA")));
            gmidjTextField.setText(rs.getString("GMID"));
            niceIDjTextField.setText(rs.getString("NICEID"));
            loginAvayajTextField.setText(Integer.toString(rs.getInt("LOGIN_AVAYA")));
            networkjTextField.setText(rs.getString("NETWORK"));
            emailjTextField.setText(rs.getString("EMAIL"));
            ccmsjTextField.setText(Integer.toString(rs.getInt("IDCCMS")));
            idSpreadFastTextField2.setText(rs.getString("ID_SPREADFAST"));
            gminjTextField.setText(Integer.toString(rs.getInt("GMIN")));

        } catch (SQLException ex) {
            Logger.getLogger(Funcionarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void verificaCamposVazios() {


        if (matriculajTextField.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "O campo MATRÍCULA é obrigatório", "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            matriculajTextField.grabFocus();
            operador = -1;

        } else if (nomejTextField.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "O campo NOME é obrigatório", "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            nomejTextField.grabFocus();
            operador = -1;

        } else if (hiredatejFormattedTextField.getText().equals("  /  /    ")) {

            JOptionPane.showMessageDialog(null, "O campo DATA ADMISSÃO é obrigatório", "Mensagem do sistema", JOptionPane.WARNING_MESSAGE);
            hiredatejFormattedTextField.grabFocus();
            operador = -1;

        } else {

            operador = 1;
        }

    }

